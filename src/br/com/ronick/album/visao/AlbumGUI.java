    private void atualiza() {
        String[] cposIrmao = {"ID", "Nome", "Contato"};
        List<Irmao> irmaos = IrmaoDAO.selecionarTodos();
        String[][] dadosIrmaos;
        dadosIrmaos = new String[irmaos.size()][3];
        int posicao = 0;
        for (Irmao ir : irmaos) {
            String[] umIrmao = new String[3];
            umIrmao[0] = String.valueOf(ir.getId());
            umIrmao[1] = ir.getNome();
            umIrmao[2] = ir.getContato();
            dadosIrmaos[posicao++] = umIrmao;
        }
        DefaultTableModel modelo = new DefaultTableModel(
                dadosIrmaos, cposIrmao);
        jtIrmaos.setModel(modelo);
        String[] cposFigurinha = {"Número", "Página", "Descr.", "Qtde"};
        List<Figurinha> figurinhas = FigurinhaDAO.selecionarPorPagina(pagina);
        String[][] dadosFigurinhas;
        dadosFigurinhas = new String[figurinhas.size()][4];
        posicao = 0;
        for (Figurinha fig : figurinhas) {
            String[] umaFigurinha = new String[4];
            umaFigurinha[0] = String.valueOf(fig.getNumero());
            umaFigurinha[1] = String.valueOf(fig.getPagina());
            umaFigurinha[2] = fig.getDescricao();
            umaFigurinha[3] = String.valueOf(fig.getQuantidade());
            dadosFigurinhas[posicao++] = umaFigurinha;
        }
        DefaultTableModel modeloFig = new DefaultTableModel(
                dadosFigurinhas, cposFigurinha);
        jTable1.setModel(modeloFig);
    }

    public int retornaQtde(String id) {
        List<Figurinha> coletaID = FigurinhaDAO.selecionarPorId(Integer.parseInt(id));
        int qtde = 0;
        for (Figurinha colId : coletaID) {
            qtde = colId.getQuantidade();
        }
        return qtde;
    }

    public void adicionarFigurinha(String numero, byte qtde) {
        String descricao = JOptionPane.showInputDialog(rootPane, "Digite a descrição:", "Adicionar Figurinha", 1);
        if (descricao != null) {
            Figurinha figurinha = new Figurinha(Integer.parseInt(numero), (byte) pagina, descricao, qtde);
            FigurinhaDAO.inserir(figurinha);
        }
    }

    public void alteraQtde(String id, int operacao) {
        List<Figurinha> figTroca = FigurinhaDAO.selecionarPorId(Integer.parseInt(id));
        int numero, qtde;
        byte pagina;
        String descricao;
        Figurinha figNova = null;
        for (Figurinha fig : figTroca) {
            numero = fig.getNumero();
            pagina = fig.getPagina();
            descricao = fig.getDescricao();
            qtde = fig.getQuantidade() + operacao;
            figNova = new Figurinha(numero, pagina, descricao, (byte) qtde);
        }
        FigurinhaDAO.alterar(figNova);
    }


    private void jbDescricaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDescricaoActionPerformed
        String id = JOptionPane.showInputDialog(rootPane, "Digite o numero do ID a ser alterado:", "Alterar Descrição", 1);
        String descricao = JOptionPane.showInputDialog(rootPane, "Digite a nova descrição:", "Alterar Descrição", 1);
        Figurinha figurinha;
        if (id != null && descricao != null) {
            if (retornaQtde(id) > 0) {
                figurinha = new Figurinha(Integer.parseInt(id), (byte) pagina, descricao, (byte) retornaQtde(id));
                FigurinhaDAO.alterar(figurinha);
                atualiza();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Você não possui esta Figurinha");
            }
        }
    }//GEN-LAST:event_jbDescricaoActionPerformed

    private void jbAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAdicionarActionPerformed
        String numero = JOptionPane.showInputDialog(rootPane, "Digite o numero da Figurinha:", "Adicionar Figurinha", 1);
        if (numero != null) {
            List<Figurinha> figurinha = FigurinhaDAO.selecionarPorId(Integer.parseInt(numero));
            for (Figurinha fig : figurinha) {
                if (fig.getQuantidade() == 0) {
                    adicionarFigurinha(numero, (byte) 1);
                } else {
                    alteraQtde(numero, 1);
                }
            }
        }
        atualiza();
    }//GEN-LAST:event_jbAdicionarActionPerformed


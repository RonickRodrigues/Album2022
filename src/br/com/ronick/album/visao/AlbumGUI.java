    private void jbRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRetirarActionPerformed
        String id = JOptionPane.showInputDialog(rootPane, "Digite o numero do ID a ser removido:", "Retirar figurinha", 1);
        if (id != null) {
            int testeFigurinha = retornaQtde(id);
            if (testeFigurinha == 1) {
                Figurinha figurinha = new Figurinha(Integer.parseInt(id), (byte) pagina, "Indefinida", (byte) 0);
                FigurinhaDAO.alterar(figurinha);
            } else if (testeFigurinha > 1) {
                alteraQtde(id, -1);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Você não possui esta figurinha");
            }
        }
        atualiza();
    }//GEN-LAST:event_jbRetirarActionPerformed

    private void jbTrocarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbTrocarActionPerformed
        String id = JOptionPane.showInputDialog(rootPane, "Digite o numero da Figurinha para trocar:",
                "Trocar figurinha", 1);
        String irmao = JOptionPane.showInputDialog(rootPane, "Digite o nome do irmão que irá trocar:",
                "Trocar figurinha", 1);
        if (id != null && irmao != null) {
            if (retornaQtde(id) > 0) {
                List<Irmao> irmaoTroca = IrmaoDAO.selecionarPorNome(irmao);
                if (irmaoTroca.size() == 1) {
                    String idNovo = JOptionPane.showInputDialog(rootPane, "Digite o numero da Figurinha que Recebeu:",
                            "Trocar figurinha", 1);
                    int testeQtdeId = retornaQtde(id);
                    int testeQtdeIdNovo = retornaQtde(idNovo);
                    if (testeQtdeId == 1 && testeQtdeIdNovo == 0) {
                        Figurinha figurinha = new Figurinha(Integer.parseInt(id), (byte) pagina, "Indefinida", (byte) 0);
                        FigurinhaDAO.alterar(figurinha);
                        adicionarFigurinha(idNovo, (byte) 1);
                    } else if (testeQtdeId == 1 && testeQtdeIdNovo > 0) {
                        Figurinha figurinha = new Figurinha(Integer.parseInt(id), (byte) pagina, "Indefinida", (byte) 0);
                        FigurinhaDAO.alterar(figurinha);
                        alteraQtde(idNovo, 1);
                    } else if (testeQtdeId > 1 && testeQtdeIdNovo == 0) {
                        alteraQtde(id, -1);
                        adicionarFigurinha(idNovo, (byte) 1);
                    } else {
                        alteraQtde(id, -1);
                        alteraQtde(idNovo, 1);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, String.format("Você não possui um irmão chamado %s", irmao));
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Você não possui essa figurinha");
            }
        }
        atualiza();
    }//GEN-LAST:event_jbTrocarActionPerformed

